function solution(letter) {
    var morse = { 
    '.-':'a','-...':'b','-.-.':'c','-..':'d','.':'e','..-.':'f',
    '--.':'g','....':'h','..':'i','.---':'j','-.-':'k','.-..':'l',
    '--':'m','-.':'n','---':'o','.--.':'p','--.-':'q','.-.':'r',
    '...':'s','-':'t','..-':'u','...-':'v','.--':'w','-..-':'x',
    '-.--':'y','--..':'z' }
    // letter문자열을 공백으로 나눈 후 돌며 morse에 넣었을 때 값을 반환해 문자열로 변환함.
    return letter.split(" ").map((item)=>morse[item]).join("");
}